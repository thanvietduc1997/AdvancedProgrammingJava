    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        String name    = jTextFieldFullName.getText();
        String sex     = (String) jComboBoxSex.getSelectedItem();
        String dob     = utils.convertJavaUtilDateToString(jDateChooserDateOfBirth.getDate(), utils.mySQLDateFormat);
        String addr    = jTextFieldAddress.getText();
        String email   = jTextFieldEmail.getText();
        String usrname = jTextFieldUsername.getText();
        String pass    = jTextFieldPassword.getText();
        String phoneno = jTextFieldPhoneNumber.getText();
        
        String[] inputString = { name, sex, dob, addr, phoneno, email, usrname, pass };
        
        String addQuery = "INSERT INTO 8gQHxi21p3.tbl_register (`fullname`, "
                + "`sex`, "
                + "`date_of_birth`, "
                + "`home_address`, "
                + "`phone_no`, "
                + "`email`, "
                + "`username`, "
                + "`password`"
                + ")"
                + " VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
      
        utils.updateQueryWithPreparedStatment(addQuery, inputString);
        
        setDefaultView();
        String[] tb_columns = { "Mã khách hàng", "Tên khách hàng", "Giới tính", "Ngày sinh", "Đại chỉ", "Số điện thoại", "Email", "Tên đăng nhập", "Mật khẩu" };
        String[] db_columns = { "customer_id", "fullname", "sex", "date_of_birth", "home_address", "phone_no", "email", "username", "password" };
        ResultSet rs = utils.executeQuery("SELECT * "
                                        + "FROM "
                                        + utils.getDBName() + ".tbl_register;");
        utils.tableRender(jTableCustomer, tb_columns, db_columns, rs);
        
        JOptionPane.showMessageDialog(null, "Thêm người dùng mới thành công", "Thông báo", 1);
        setDefaultView();
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        int rowIdx = jTableCustomer.getSelectedRow();
        String customer_id = (String)jTableCustomer.getValueAt(rowIdx, 0);
        String updateQuery = "UPDATE  "
                + utils.getDBName()
                +".`tbl_register` SET "
                + "`fullname` = ?, "
                + "`sex` = ?, "
                + "`date_of_birth` = ?, "
                + "`home_address` = ?, "
                + "`phone_no` = ?, "
                + "`email` = ?, "
                + "`username` = ?, "
                + "`password` = ? "
                + "WHERE (`customer_id` = ?);";
        String[] data = {
            jTextFieldFullName.getText(),
            jComboBoxSex.getSelectedItem().toString(),
            utils.convertJavaUtilDateToString(jDateChooserDateOfBirth.getDate(), utils.getMySQLDateFormat()),
            jTextFieldAddress.getText(),
            jTextFieldPhoneNumber.getText(),
            jTextFieldEmail.getText(),
            jTextFieldUsername.getText(),
            jTextFieldPassword.getText(),
            customer_id
        };
        utils.updateQueryWithPreparedStatment(updateQuery, data);
        JOptionPane.showMessageDialog(null, "Sửa thông tin khách hàng thành công", "Thông báo", 1);
        
        jTableCustomer.setValueAt(jTextFieldFullName.getText(), rowIdx, 1);
        jTableCustomer.setValueAt(jComboBoxSex.getSelectedItem(), rowIdx, 2);
        jTableCustomer.setValueAt(utils.convertJavaUtilDateToString(jDateChooserDateOfBirth.getDate(), utils.getMySQLDateFormat()), rowIdx, 3);
        jTableCustomer.setValueAt(jTextFieldAddress.getText(), rowIdx, 4);
        jTableCustomer.setValueAt(jTextFieldPhoneNumber.getText(), rowIdx, 5);
        jTableCustomer.setValueAt(jTextFieldEmail.getText(), rowIdx, 6);
        jTableCustomer.setValueAt(jTextFieldUsername.getText(), rowIdx, 7);
        jTableCustomer.setValueAt(jTextFieldPassword.getText(), rowIdx, 8);
        
        setDefaultView();
    }//GEN-LAST:event_jButtonEditActionPerformed

