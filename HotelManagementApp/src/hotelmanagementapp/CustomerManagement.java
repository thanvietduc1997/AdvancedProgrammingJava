    private void jTableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCustomerMouseClicked
        // TODO add your handling code here:
        jButtonEdit.setEnabled(true);
        jButtonDelete.setEnabled(true);
        jButtonAdd.setEnabled(false);
        
        int rowIndex = jTableCustomer.getSelectedRow();
        String fullname = (String) jTableCustomer.getValueAt(rowIndex, 1);
        String sex = (String) jTableCustomer.getValueAt(rowIndex, 2);
        String dob = (String) jTableCustomer.getValueAt(rowIndex, 3);
        String addr = (String) jTableCustomer.getValueAt(rowIndex, 4);
        String phoneno = (String) jTableCustomer.getValueAt(rowIndex, 5);
        String email = (String) jTableCustomer.getValueAt(rowIndex, 6);
        String username = (String) jTableCustomer.getValueAt(rowIndex, 7);
        String password = (String) jTableCustomer.getValueAt(rowIndex, 8);
        
        jTextFieldFullName.setText(fullname);
        if(null == sex) jComboBoxSex.setSelectedIndex(2);
        else switch (sex) {
            case "Nam":
                jComboBoxSex.setSelectedIndex(0);
                break;
            case "Nữ":
                jComboBoxSex.setSelectedIndex(1);
                break;
            default:
                jComboBoxSex.setSelectedIndex(2);
                break;
        }
        Date date = Date.valueOf(dob);
        jDateChooserDateOfBirth.setDate(date);
        jTextFieldAddress.setText(addr);
        jTextFieldPhoneNumber.setText(phoneno);
        jTextFieldEmail.setText(email);
        jTextFieldUsername.setText(username);
        jTextFieldPassword.setText(password);
    }//GEN-LAST:event_jTableCustomerMouseClicked

